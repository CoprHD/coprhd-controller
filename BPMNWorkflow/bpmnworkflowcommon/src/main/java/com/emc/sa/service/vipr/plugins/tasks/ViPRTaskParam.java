package com.emc.sa.service.vipr.plugins.tasks;



public class ViPRTaskParam {
	String virtualArray;
	String virtualPool;
	String project;
	
	public String getVirtualArray() {
		return virtualArray;
	}
	public void setVirtualArray(String virtualArray) {
		this.virtualArray = virtualArray;
	}
	public String getVirtualPool() {
		return virtualPool;
	}
	public void setVirtualPool(String virtualPool) {
		this.virtualPool = virtualPool;
	}
	public String getProject() {
		return project;
	}
	public void setProject(String project) {
		this.project = project;
	}


}
